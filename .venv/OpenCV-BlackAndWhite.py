# Импортируем библиотеку OpenCV для работы с изображениями
import cv2

# Загружаем изображение 'test.jpg' с диска в переменную image
# Функция imread считывает файл. Если файла нет, вернется None.
image = cv2.imread('test.jpg')

# Преобразуем загруженное изображение из цветного (BGR) в оттенки серого (Grayscale)
# cv2.COLOR_BGR2GRAY — это флаг, указывающий тип преобразования
gray_image = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)

# Применяем фильтр размытия по Гауссу (Gaussian Blur) к черно-белому изображению
# (15, 15) — это размер ядра (kernel size). Чем больше числа, тем сильнее размытие.
# Числа обязательно должны быть нечетными (например, 3, 5, 7, 15...).
# 0 — это стандартное отклонение по оси X (0 означает, что оно рассчитывается автоматически)
blurred_image = cv2.GaussianBlur(gray_image, (15, 15), 0)

# Создаем окно и показываем в нем оригинальное изображение
cv2.imshow('Original Image', image)

# Создаем второе окно и показываем черно-белый вариант
cv2.imshow('Gray Image', gray_image)

# Создаем третье окно и показываем размытый результат
cv2.imshow('Blurred Image', blurred_image)

# Ждем нажатия любой клавиши на клавиатуре (0 означает бесконечное ожидание)
# Это нужно, чтобы окна не закрылись мгновенно после открытия
cv2.waitKey(0)

# После нажатия клавиши закрываем все открытые окна OpenCV и освобождаем память
cv2.destroyAllWindows()